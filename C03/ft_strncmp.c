int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n == 0)
		return (0);
	while (n - 1 > 0 && *s1 != '\0' && *s1 == *s2 && *s2 != '\0')
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 - *s2);
}
